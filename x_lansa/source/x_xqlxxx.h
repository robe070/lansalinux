#define RETURN_ACTUAL_KEY(_TO,_FROM,_SKIP) \
{ \
   X_PVCHAR pvchPtr = (X_PVCHAR) _TO; \
   memset(_TO, LOVAL, (sizeof(_FROM) + _SKIP)); \
   memcpy( &(pvchPtr[_SKIP]), &(_FROM), sizeof(_FROM) ); \
   if ( _SKIP == X_XQL_PARTITION_SIZE ) \
   { \
      memcpy( _TO, pX_Ids->vchPartition, X_XQL_PARTITION_SIZE ); \
   } \
}

