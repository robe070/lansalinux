#define MAKE_STANDARD_KEY(_S) \
{ \
   X_VCHAR vchTempString[sizeof(_S)]; \
   X_LONG sLen =  X_LONG_SIZE_T_CAST( strlen(_S) ); \
   memset(vchTempString, LOVAL, sizeof(vchTempString)); \
   if (sLen > 0) \
   { \
      X_LONG sX = sLen - 1; \
      memcpy(vchTempString, _S, sLen); \
      for ( ; sX >= 0; sX--) \
      { \
         if (vchTempString[sX] == LOVAL) continue; \
         if (vchTempString[sX] != BLANK) break; \
         vchTempString[sX] = LOVAL; \
      } \
   } \
   memcpy(_S, vchTempString, sizeof(vchTempString)); \
}

#define X_XQL_PARTITION_SIZE 3 + 1 /* Below, 3 isused instead of X_PARTITION_NAME_LEN; */
