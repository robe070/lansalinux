/* These 2 macros are basically the same code with calls   */
/* to either X_Select_ISAM_Set() or X_Chain_ISAM_Record()  */
/* They have thus faronly been implemented for files being */
/* redirected to the server, but could be used to replace  */
/* all similar code.                                       */
#define SELECT_ISAM_SET(_ISAMSET, _PDATA, _PKEY, \
                        _SELECTFUNC, _SELECTVAL,\
                        _NEWKEYLEN,_NEWFILEVER, \
						      _OLDKEYLEN,_OLDFILEVER) \
         ulInputKeyLength = _NEWKEYLEN; \
         chRetCode = X_Select_ISAM_Set (pX_Ids, \
                                        pX_Pro, \
                                        pX_Fun, \
                                        _NEWFILEVER, \
                                        &(_ISAMSET), \
                                        vchKeysPartFile, \
                                        vchDataPartFile, \
                                        ulInputKeyLength, \
                                        (X_PVCHAR) &(NewKey), \
                                        (X_PVCHAR) _PDATA, \
                                        _SELECTFUNC, \
                                        _SELECTVAL, \
                                        pX_NULL, \
                                        0); \
         switch ( chRetCode ) \
         { \
            case X_XQL_OKAY: \
               RETURN_ACTUAL_KEY(_PKEY,NewKey,X_XQL_PARTITION_SIZE) \
               break; \
            case X_XQL_FILE_ERROR: \
               chRetCode = X_Select_ISAM_Set (pX_Ids, \
                                              pX_Pro, \
                                              pX_Fun, \
                                              _NEWFILEVER, \
                                              &(_ISAMSET), \
                                              vchKeysSysFile, \
                                              vchDataSysFile, \
                                              ulInputKeyLength, \
                                              (X_PVCHAR) &(NewKey), \
                                              (X_PVCHAR) _PDATA, \
                                              _SELECTFUNC, \
                                              _SELECTVAL, \
                                              pX_NULL, \
                                              0); \
               if ( chRetCode == X_XQL_OKAY ) \
               { \
                  RETURN_ACTUAL_KEY(_PKEY,NewKey,X_XQL_PARTITION_SIZE) \
               } \
               break; \
            case X_XQL_VERSION_ERROR: \
            { \
               ulInputKeyLength = _OLDKEYLEN; \
               chRetCode = X_Select_ISAM_Set (pX_Ids, \
                                              pX_Pro, \
                                              pX_Fun, \
                                              _OLDFILEVER, \
                                              &(_ISAMSET), \
                                              vchKeysPartFile, \
                                              vchDataPartFile, \
                                              ulInputKeyLength, \
                                              (X_PVCHAR) &(Key), \
                                              (X_PVCHAR) _PDATA, \
                                              _SELECTFUNC, \
                                              _SELECTVAL, \
                                              vchLANSAXVersion, \
                                              sizeof(vchLANSAXVersion)); \
               if ( chRetCode == X_XQL_OKAY ) \
               { \
                  RETURN_ACTUAL_KEY(_PKEY,Key,sZero) \
               } \
               else if ( chRetCode == X_XQL_VERSION_ERROR ) \
               { \
                  X_Fatal_Error (pX_Ids, \
                     pX_Pro, \
                     pX_Fun, \
                     "GetIndexHeader", \
                     X_TEXT(349), \
                     X_BuildNumber.pvchRelease, \
                     vchLANSAXVersion); \
               } \
               break; \
            } \
         }

